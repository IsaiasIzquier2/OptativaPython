# Crea un programa que reciba 5 números del usuario y muestre el mayor de todos por pantalla.

count = 0
topnum = 0

while (count < 5):
    num = int(input("Introduce un número:"))

    count == count+1

    if (num >= num):
        topnum = topnum + num

else:
    print(f"El númro más grande es el {topnum} ")